#!/usr/bin/env python3
"""Generate docs from source (primitives, modules, stdlib).

Usage:
  python3 scripts/gen_docs.py          # write docs/generated/*.md
  python3 scripts/gen_docs.py --check  # fail if generated files are stale
"""

from __future__ import annotations

import argparse
import re
import sys
from collections import defaultdict
from dataclasses import dataclass
from pathlib import Path

ROOT = Path(__file__).resolve().parent.parent
SRC = ROOT / "src"
STD = ROOT / "lib" / "std"
OUT = ROOT / "docs" / "generated"

GENERATED_BANNER = "<!-- AUTO-GENERATED by scripts/gen_docs.py — do not edit -->\n"

PRIMITIVE_RE = re.compile(r'primitives_\.add\s*\(\s*"([^"]+)"')
FFI_ADD_RE = re.compile(r'^\s+add\s*\(\s*"([^"]+)"', re.MULTILINE)
MODULE_RE = re.compile(r"^export\s+module\s+([\w.]+)\s*;", re.MULTILINE)
EXPORT_START_RE = re.compile(r"^\(export\b", re.MULTILINE)


